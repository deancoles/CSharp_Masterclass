// One Line IF statements


int month = 5;
string monthName;

// Will need brackets as it has two lines of code
if (month == 1)
{
    monthName = "January";
    month = 2;
}

// Curly brackets are only needed for two or more lines of code
else if (month == 2)
    monthName = "February";
else if (month == 3)
    monthName = "March";
else
    monthName = "Unknown";
