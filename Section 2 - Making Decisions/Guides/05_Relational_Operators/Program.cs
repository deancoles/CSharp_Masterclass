// Relational Operators

int num1 = 5;
int num2 = 6;
int age = 37;

// list of operators    <   <=  >   >=  ==

// < less than 
bool isLess = num1 <  num2;

// <= less or equal
bool isLessEquals = num1 <= num2;

// > more than 
bool isMore = num1 > num2;

// >= greater or equal
bool isMoreEquals = num1 >= num2;

// == equal
bool isSame = num1 == num2;