// For Loops

// Run code block and increment by 1 until counter is 10
//
//      define         condition      increment

for (int counter = 0; counter <= 10; counter++)
{
    Console.WriteLine($"Counter is {counter}");
}

Console.ReadKey();

// Run code block and decrement by 1 until counter is 1
//
//      define           condition      decrement
for (int counter2 = 10; counter2 >= 0; counter2--)
{
    Console.WriteLine($"Counter is {counter2}");
}

Console.ReadKey();