// Implicitly Typed variables vs. Explicitly Typed Variables

// implicitly typed variables, will assign a data type automatically based on the data received
var myFavouriteGenre = "Detective";

var myFavouriteNumber = 13;

var yourFavouriteNumber = 14;

var combinedNumber = myFavouriteNumber + yourFavouriteNumber;
Console.WriteLine($"Our two numbers add up to {combinedNumber}");


// explicitly typed variable
string myName = "Dean";